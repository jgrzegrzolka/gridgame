const PALETTE = new Set(['red', 'white', 'blue', 'green', 'yellow', 'black', 'orange']);

const path = 'flags/countries.json';
const countries = JSON.parse(readFileSync(path, 'utf-8'));

const missing = [];
for (const c of countries) {
  const tagged = COLORS[c.code];
  if (!tagged) {
    missing.push(c.code);
    c.colors = [];
    continue;
  }
  // Dedup while preserving order; drop anything outside the palette.
  const seen = new Set();
  /** @type {string[]} */
  const filtered = [];
  for (const color of tagged) {
    if (!PALETTE.has(color)) {
      throw new Error(`Color '${color}' for code '${c.code}' is not in the palette`);
    }
    if (seen.has(color)) continue;
    seen.add(color);
    filtered.push(color);
  }
  c.colors = filtered;
}

writeFileSync(path, JSON.stringify(countries, null, 2) + '\n');

if (missing.length) {
  console.warn(`Untagged country codes (got empty colors arrays):`, missing);
} else {
  console.log(`Tagged colors on all ${countries.length} countries.`);
}
